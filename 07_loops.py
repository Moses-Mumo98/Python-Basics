
# while loop
i = 1
while i <= 10:
    print("*" * i)
    i += 1

print("Done")


# for loops
for char in "Python":
    print(char)

# loop over an array [list]
fruits = ["Apple", "Oranges", "Avocado"]

for fruit in fruits:
    print(fruit)

# find the total price
prices = [10, 20, 30]

total = 0
for price in prices:
    total += price

print(total)
